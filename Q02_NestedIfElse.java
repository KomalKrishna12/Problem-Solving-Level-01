public class Q02_NestedIfElse {
    
    public static void main(String[] args) {
        
        int n1 = 30;
        int n2 = 30;
        if(n1 == n2){
            System.out.println(n1 + " is equal to " + n2);
        }
        else if(n1 > n2){
            System.out.println(n1 + " is greater than " + n2);
        }
        else{
            System.out.println(n2 + " is greater than " + n1);
        }

    }

}
