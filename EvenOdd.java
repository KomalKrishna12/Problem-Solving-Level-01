public class EvenOdd {
    
    public static void main(String[] args) {
        
        int x = 11;
        if(x % 2 == 0){
            System.out.println(x + " even number! ");
        }
        else{
            System.out.println(x + " odd number! ");
        }
        System.out.println("This is if else statement.!");

    }

}
